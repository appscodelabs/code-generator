package util

import (
	"k8s.io/gengo/namer"
	"k8s.io/gengo/types"
)

var goKeywords = map[string]string{
	"break":       "break_",
	"case":        "case_",
	"chan":        "chan_",
	"const":       "const_",
	"continue":    "continue_",
	"default":     "default_",
	"defer":       "defer_",
	"else":        "else_",
	"fallthrough": "fallthrough_",
	"for":         "for_",
	"func":        "func_",
	"go":          "go_",
	"goto":        "goto_",
	"if":          "if_",
	"import":      "import_",
	"interface":   "interface_",
	"main":        "main_",
	"map":         "map_",
	"package":     "package_",
	"range":       "range_",
	"return":      "return_",
	"select":      "select_",
	"struct":      "struct_",
	"switch":      "switch_",
	"type":        "type_",
	"var":         "var_",
}

// GoSafeNamer adds _ as suffix if name generated by the delegate is a Go keyword.
type GoSafeNamer struct {
	Delegate namer.Namer
}

// Name provides the requested name for a type.
func (n *GoSafeNamer) Name(t *types.Type) string {
	s := n.Delegate.Name(t)
	if replace, ok := goKeywords[s]; ok {
		return replace
	}
	return s
}

// GoSafePackageName provides a package name that is does not conflict with Go reserved words
func GoSafePackageName(pkg string) string {
	if replace, ok := goKeywords[pkg]; ok {
		return replace
	}
	return pkg
}
